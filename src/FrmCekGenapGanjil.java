    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String input = jTextField1.getText().trim();

    // Validasi jika kosong
    if (input.isEmpty()) {
        jTextField1.setText("Masukkan angka!");
        return;
    }

    try {
        int angka = Integer.parseInt(input);
        String hasil = "";

        // Cek Genap/Ganjil
        if (angka % 2 == 0) {
            hasil += angka + " adalah GENAP. ";
        } else {
            hasil += angka + " adalah GANJIL. ";
        }

        // Cek Bilangan Prima
        boolean prima = true;
        if (angka <= 1) prima = false;
        else {
            for (int i = 2; i <= Math.sqrt(angka); i++) {
                if (angka % i == 0) {
                    prima = false;
                    break;
                }
            }
        }

        if (prima) hasil += "Dan ini adalah BILANGAN PRIMA.";
        else hasil += "Dan ini BUKAN bilangan prima.";

        jLabel4.setText("<html>" + hasil.replaceAll("\\. ", ".<br>") + "</html>");

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this,
        "Input tidak valid! Masukkan hanya angka.",
        "Error",
        JOptionPane.ERROR_MESSAGE);
    jLabel4.setText("<html><font color='red'>Input tidak valid!</font></html>");
    }    // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

